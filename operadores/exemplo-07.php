<?php 
///Operador de Incremento

$a = 10;

//Incrementa a variavel após a execução;
echo $a++;

echo "<br>";

echo $a;

echo "<br>";

//Incrementa a variavel antes da execução;
echo ++$a;

echo "<br>";

//Decrementa a variavel após a execução;
echo $a--;

echo "<br>";

echo $a;

echo "<br>";

//Decrementa a variavel antes da execução;
echo --$a;

echo "<br>";
 ?>