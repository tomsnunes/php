<?php 

///Operadores de Comparação
$a = 30;
$b = 55;

//Maior
var_dump($a > $b);
echo "<br>";

//Maior ou Igual
var_dump($a >= $b);
echo "<br>";

//Menor
var_dump($a < $b);
echo "<br>";

//Menor ou Igual
var_dump($a <= $b);
echo "<br>";

//Comparação de Igualdade de Valor
var_dump($a == $b);
echo "<br>";

//Comparação de Igualdade de Identidade
var_dump($a === $b);
echo "<br>";

//Comparação de Diferença de Valor
var_dump($a != $b);

//Comparação de Diferença de Identidade
var_dump($a !== $b);

 ?>